#Get two values from user
x = input("Enter x value:")
y = input("Enter y value:")

#Print entered value
print(f"""The entered x value is: {x}""")
print(f"""The entered y value is: {y}""")

#Convert int
x = int(x)
y = int(y)

#Print types value
print(f"""The type of entered x value is: {type(x)}""")
print(f"""The type of entered y value is: {type(y)}""")

#Print the sum of x and y
sum = x + y
print(f"""x + y = {sum}""")

#Convert type of sum as int
sum = int(sum)
print(f"""The type of summation value is: {type(sum)}""")
